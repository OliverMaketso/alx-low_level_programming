/**
 * @brief Adds two integers and returns the result.
 *
 * @param a The first integer to add.
 * @param b The second integer to add.
 * @return The sum of the two integers.
 */
int add(int a, int b) {
  /* Betty documenting style */
  /* Function's name: add */
  /* Function's parameters: a, b */
  /* Function's return value: The sum of the two integers */

  return a + b;
}
